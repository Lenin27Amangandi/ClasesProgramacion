public class ForNombreApellidoCZ {
    public void InicialNombreApellido(){
        for(int i=0;i<4;i++){
            System.out.print("*");           
        }
        System.out.print(" ");
        
        for(int p=0;p<4;p++){
            System.out.print("*");
        }
        System.out.println();

        for (int f = 0; f < 1; f++) {
            System.out.print("*");
            for (int e = 0; e < 6; e++) {
                System.out.print(" ");
            }
            System.out.println("*");
        }

        for (int f = 0; f < 1; f++) {
            System.out.print("*");
            for (int e = 0; e < 5; e++) {
                System.out.print(" ");
            }
            System.out.println("*");
        }

        for(int q=0;q<4;q++){
            System.out.print("*");           
        }
        System.out.print(" ");
        for(int o=0;o<4;o++){
            System.out.print("*");
        }
    }
}

