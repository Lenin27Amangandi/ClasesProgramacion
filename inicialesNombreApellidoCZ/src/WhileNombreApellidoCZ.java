public class WhileNombreApellidoCZ {
    public void InicialNombreApellido(){
        int i=0;
        while(i<4){
            System.out.print("*");
            i++;
        }

        int j=0;
        System.out.print(" ");
        while(j<4){
            System.out.print("*");
            j++;
        }
        System.out.println();

        int f=0;
        while(f<1){
            System.out.print("*");
            f++;
            int e=0;
            while(e<6){
                System.out.print(" ");
                e++;
            }
            System.out.println("*");
        }

        int y=0;
        while(y<1){
            System.out.print("*");
            y++;
            int x=0;
            while(x<5){
                System.out.print(" ");
                x++;
            }
            System.out.println("*");
        }

        int k=0;
        while(k<4){
            System.out.print("*");
            k++;
        }
        
        int l=0;
        System.out.print(" ");
        while(l<4){
            System.out.print("*");
            l++;
        }
    }     
}
