public class WhileApellidoZ {
    public void InicialApellido(){

        int i=0;
        while(i<7){
            System.out.print("*");
            i++;
        }
        System.out.println();

        int j=5;
        while(j>0){
            int l=0;
            while (l<j) {
                System.out.print(" ");
                l++;
            }
            System.out.println("*");
            j--;
        }

        int k=0;
        while(k<7){
            System.out.print("*");
            k++;
        }
    }
}
