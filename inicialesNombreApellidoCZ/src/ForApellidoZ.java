public class ForApellidoZ {
    public void InicialApellido(){
        for(int i=0;i<7;i++){
            System.out.print("*");
        }
        System.out.println();
    
        for(int j=5;j>0;j--){ 
            for(int p=0;p<j;p++){
                System.out.print(" ");
            }         
            System.out.println("*");
        }
        
        for(int k=0;k<7;k++){
            System.out.print("*");
        }
    }
}
