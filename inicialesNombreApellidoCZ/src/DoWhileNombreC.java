public class DoWhileNombreC {
    public void InicialNombre(){
        int i = 0;
        do{
            System.out.print("*");
            i++;
        }while(i<6);

        int j=0;
        do{
            System.out.println("*");
            j++;
        }while(j<6);

        int k=0;
        do{
            System.out.print("*");
            k++;
        }while(k<7);
    }
}
